// Generates lib/countries.ts from the flag assets so the picker can only ever
// offer a country that has a real PNG (no 404s). Source of truth:
//   - public/badges/flags/<code>.png  → the codes we actually ship art for
//   - public/badges/flags/countries.json → ISO code → English name
// We keep only sovereign-ish ISO 3166-1 alpha-2 entries: drop the sub-region
// and supranational pseudo-flags (gb-eng, gb-sct, eu, …) and deprecated codes,
// so the list reads as a clean country picker rather than a flag catalogue.
//
// Run:  node scripts/gen-countries.mjs   (re-run if flag assets change)

import { readdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const flagsDir = join(root, "public", "badges", "flags");

// Pseudo / sub-region / deprecated codes that have art but aren't real countries
// a user should pick as "their" flag.
const EXCLUDE = new Set([
  "eu", // European Union (supranational)
  "an", // Netherlands Antilles (dissolved 2010)
  "gb-eng", "gb-nir", "gb-sct", "gb-wls", // UK home nations — gb covers them
]);

const names = JSON.parse(readFileSync(join(flagsDir, "countries.json"), "utf8"));
// Normalise the name map to lowercase codes for lookup.
const nameByCode = new Map(
  Object.entries(names).map(([code, name]) => [code.toLowerCase(), name]),
);

const codes = readdirSync(flagsDir)
  .filter((f) => f.endsWith(".png"))
  .map((f) => f.slice(0, -4).toLowerCase())
  .filter((code) => !EXCLUDE.has(code) && !code.includes("-"))
  .filter((code) => nameByCode.has(code));

const list = codes
  .map((code) => ({ code, name: nameByCode.get(code) ?? "" }))
  .sort((a, b) => a.name.localeCompare(b.name, "en"));

const header = `// AUTO-GENERATED by scripts/gen-countries.mjs — do not edit by hand.
// ISO 3166-1 alpha-2 codes that have a flag PNG in public/badges/flags/, paired
// with their English name. Sub-region and supranational pseudo-flags are
// excluded so this reads as a clean country picker. Re-run the script to refresh.

export interface Country {
  /** ISO 3166-1 alpha-2, lowercase — also the flag asset key (/badges/flags/<code>.png). */
  code: string;
  name: string;
}

export const COUNTRIES: readonly Country[] = ${JSON.stringify(list, null, 2)};

const BY_CODE = new Map(COUNTRIES.map((c) => [c.code, c] as const));

/** True when \`code\` is a country we ship a flag for. Case-insensitive. */
export function isValidCountry(code: string | null | undefined): boolean {
  return !!code && BY_CODE.has(code.toLowerCase());
}

/** Normalise to a valid lowercase code, or null if we don't ship it. */
export function normalizeCountry(code: string | null | undefined): string | null {
  if (!code) return null;
  const c = code.toLowerCase();
  return BY_CODE.has(c) ? c : null;
}

/** English country name for a code, or null. Case-insensitive. */
export function countryName(code: string | null | undefined): string | null {
  if (!code) return null;
  return BY_CODE.get(code.toLowerCase())?.name ?? null;
}

/**
 * Filter countries by a free-text query, matched against name and code.
 * Empty query returns the full list. Prefix matches rank above substring
 * matches so typing "ge" surfaces Georgia/Germany before Algeria.
 */
export function searchCountries(query: string): readonly Country[] {
  const q = query.trim().toLowerCase();
  if (!q) return COUNTRIES;
  const starts: Country[] = [];
  const contains: Country[] = [];
  for (const c of COUNTRIES) {
    const name = c.name.toLowerCase();
    if (name.startsWith(q) || c.code === q) starts.push(c);
    else if (name.includes(q) || c.code.startsWith(q)) contains.push(c);
  }
  return [...starts, ...contains];
}
`;

writeFileSync(join(root, "lib", "countries.ts"), header);
console.log(`Wrote lib/countries.ts with ${list.length} countries.`);
